const a = 7
let b = 3

b += a // fazer uma atribuição de adição..que seria equivalente b = b + a
console.log(b)

b -= 4 // atribuição de subtração..b = b - 4
console.log(b)

b *= 2 // multiplicação..b = b * 2
console.log(b)

b /= 2 // divisão..b = b / 2
console.log(b)

b %= 2 // modular...b = b % 2 (resto da divisão) caso par será 0 e ípar será 1
console.log(b)